"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const navItems = [
  { href: "/rak-landscape", label: "RAK Landscape", icon: MapIcon },
  { href: "/master-plan", label: "Master Plan", icon: PlanIcon },
  { href: "/asset-specs", label: "Asset Specs", icon: SpecIcon },
  { href: "/gallery", label: "Gallery", icon: GalleryIcon },
  { href: "/site-visit", label: "Site Visit", icon: CalendarIcon },
];

export default function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="flex flex-col w-[200px] min-h-screen bg-forest text-white py-8 px-5 shrink-0">
      {/* Logo */}
      <Link href="/" className="mb-10">
        <span className="font-heading text-2xl font-bold tracking-wider text-white">
          NAMOU
        </span>
      </Link>

      {/* Nav */}
      <nav className="flex flex-col gap-1">
        {navItems.map((item) => {
          const active = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-colors ${
                active
                  ? "bg-white/15 text-white font-medium"
                  : "text-white/70 hover:bg-white/10 hover:text-white"
              }`}
            >
              <item.icon className="w-4 h-4 shrink-0" />
              {item.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

